def load_summary():
    try:
        with open(SUMMARY_FILE, "r", encoding="utf-8") as f:
            return f.read().strip()
    except FileNotFoundError:
        return ""

def save_summary(summary):
    with open(SUMMARY_FILE, "w", encoding="utf-8") as f:
        f.write(summary)

def update_summary(old_summary, user_input, bot_reply):
    if old_summary == "":
        return f"Henry ne baat ki: {user_input}. Bot ne kaha: {bot_reply}"
    return old_summary + f" | Phir Henry ne baat ki: {user_input}."

