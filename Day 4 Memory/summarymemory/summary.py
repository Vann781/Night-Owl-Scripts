# ───────────────────────────────────────────
# TTS - HINDI VOICE
# ───────────────────────────────────────────
def speak(text):
    print(f"Bot: {text}")
    tts = gTTS(text=text, lang="hi", slow=False)
    tts.save("temp_audio.mp3")
    os.system("ffmpeg -y -i temp_audio.mp3 temp_audio.wav -loglevel quiet")
    data, samplerate = sf.read("temp_audio.wav")
    sd.play(data, samplerate)
    sd.wait()
    os.remove("temp_audio.mp3")
    os.remove("temp_audio.wav")

# ───────────────────────────────────────────
# PDF GENERATOR
# ───────────────────────────────────────────
def generate_pdf(summary):
    filename = f"henry_summary_{datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"
    doc = SimpleDocTemplate(filename, pagesize=letter)
    styles = getSampleStyleSheet()
    story = []

    title_style = ParagraphStyle("title", parent=styles["Title"],
        textColor=colors.HexColor("#8B0000"), fontSize=22, spaceAfter=10)
    story.append(Paragraph("Horrid Henry - Summary Memory Report", title_style))
    story.append(Spacer(1, 10))

    date_style = ParagraphStyle("date", parent=styles["Normal"], textColor=colors.grey, fontSize=10)
    story.append(Paragraph(f"Generated on: {datetime.now().strftime('%d %B %Y, %I:%M %p')}", date_style))
    story.append(Spacer(1, 20))
    story.append(Paragraph("<hr/>", styles["Normal"]))
    story.append(Spacer(1, 10))
    story.append(Paragraph("What Henry Remembers:", styles["Heading2"]))
    story.append(Spacer(1, 10))

    if summary:
        events = summary.split(" | ")
        for i, event in enumerate(events, 1):
            event_style = ParagraphStyle("event", parent=styles["Normal"],
                fontSize=11, spaceAfter=6, leftIndent=20)
            story.append(Paragraph(f"{i}. {event}", event_style))
    else:
        story.append(Paragraph("Koi yaadein nahi hain abhi tak!", styles["Normal"]))

    story.append(Spacer(1, 20))
    story.append(Paragraph("<hr/>", styles["Normal"]))
    footer_style = ParagraphStyle("footer", parent=styles["Normal"], textColor=colors.grey, fontSize=9)
    story.append(Paragraph("Generated by Horrid Henry Summary Memory Agent", footer_style))
    doc.build(story)
    return filename

